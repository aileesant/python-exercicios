numeros = []

while True:
    valor = int(input('Digite um valor: ')) #o int se põe quando é da variavel NUMERO
    if(valor == 0):
        break
    else:
        numeros.append(valor)

print(numeros)        

#foi criado um vator vazio
#um laço infinito
#quando digitar 0 acaba 
#todos os numeros digitados antes do zero estao armazenados do "prédio" numeros
#depois ele mostra os numeros digitados na tela dentro do 'numeros'

